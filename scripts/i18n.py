# ── Reverse lookup: Chinese → English ──
_EN_MAP = {v: k for k, v in _ZH_MAP.items() if len(v) > 1}  # skip single-char to avoid partial matches


class T:
    """Bilingual translator."""

    def __init__(self, lang="zh"):
        """lang: 'zh' or 'en'"""
        self._lang = lang

    def __call__(self, key):
        """Translate key. English→Chinese (zh) or Chinese→English (en)."""
        if self._lang == "zh":
            return _ZH_MAP.get(key, key)
        else:
            return _EN_MAP.get(key, key)

    @property
    def lang(self):
        return self._lang

    @staticmethod
    def detect():
        """Detect language from environment variable."""
        import os
        return os.environ.get("AGENTS_REPORT_LANG", "zh")
